namespace DodoTheGame
{
  public class ItemStack
  {
    public int itemId;
    public int count;

    public ItemStack()
    {
    }

    public ItemStack(int itemId, int count)
    {
      this.itemId = itemId;
      this.count = count;
    }

    public ItemStack Copy() => new ItemStack(this.itemId, this.count);
  }
}
